    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedrow= jTable1.getSelectedRow();

        if (selectedrow == -1) {

            if (model.getRowCount() == 0) {

               JOptionPane.showMessageDialog(null, "Öğrenci kayıt tablosunda bilgi bulanamadı...","Kayıt Bulunamadı",JOptionPane.OK_OPTION);
                setVisible(false);
                srcbil ogrenciIslemleriEkrani = new srcbil();
        ogrenciIslemleriEkrani.setVisible(true);
            }
            else{
                  JOptionPane.showMessageDialog(null, "Lütfen silinecek bir öğrenci seçin...","Öğrenci Seçim",JOptionPane.WARNING_MESSAGE);
                 setVisible(false);
        srcbil ogrenciKayitSilme = new srcbil();
        ogrenciKayitSilme.setVisible(true);
            }

        }
        else{

            int tc=(int)model.getValueAt(selectedrow, 0);

            //,ad,soyad,tc,dogumtarih,kresbaslangic,kangrubu,veliad,velisoyad,velitc,yakinlik,telno,adres
            adminislemleri1.perSil(tc);
            setVisible(false);
            
        Personelbil abc = new Personelbil();
        abc.setVisible(true);
        }
        pergor();
    }//GEN-LAST:event_jButton2ActionPerformed

