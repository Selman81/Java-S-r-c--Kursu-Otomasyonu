    public void pergor() {
        
        model.setRowCount(0);
        
        ArrayList<personel> perg = new ArrayList<personel>();
        perg = adminislemleri1.personelgoruntule();
        
        if (perg != null ) {
            
            for (personel perr : perg) {
                Object[] jtable = {perr.getPertc(),perr.getPerad(),perr.getPersoyad(),perr.getPergorev(),perr.getUcret()};
                
                model.addRow(jtable);
            }
        }
    }

